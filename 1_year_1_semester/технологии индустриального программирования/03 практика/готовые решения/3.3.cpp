// Кирилл очень любит грибы. У него есть друг Александр, 
// который периодически присылает ему координаты грибов (ячейки сетки на карте по вертикали и горизонтали), но есть проблема: У Кирилла 2 по географии.
// Необходимо помочь Кириллу и составить таблицу по точкам, в которой местоположение грибов обозначается *, 
// а остальные ячейки содержат количество грибов вокруг данной точки (как в сапёре).
// ВСЕ СИМВОЛЫ ВЫВОДЯТСЯ БЕЗ ПРОБЕЛОВ

// Формат ввода
// 3 2
// 2
// 1 1
// 2 2

// Формат вывода
// * 2
// 2 *
// 1 1
#include <iostream>
#include <vector>

using namespace std;

int main() {
    int N, M, W;
    cin >> N >> M >> W;

    vector<vector<int>> field(N, vector<int>(M, 0));

    // Заполняем поле грибами и увеличиваем счетчики соседних ячеек
    for (int i = 0; i < W; i++) {
        int x, y;
        cin >> x >> y;
        x--; // Уменьшаем координаты на 1, так как индексы начинаются с 0
        y--;

        field[x][y] = -1; // Устанавливаем гриб
        for (int dx = -1; dx <= 1; dx++) {
            for (int dy = -1; dy <= 1; dy++) {
                int nx = x + dx;
                int ny = y + dy;
                if (nx >= 0 && nx < N && ny >= 0 && ny < M && field[nx][ny] != -1) {
                    field[nx][ny]++;
                }
            }
        }
    }

    // Выводим результат
    for (int i = 0; i < N; i++) {
        for (int j = 0; j < M; j++) {
            if (field[i][j] == -1) {
                cout << "*";
            } else {
                cout << field[i][j];
            }
        }
        cout << endl;
    }

    return 0;
}
