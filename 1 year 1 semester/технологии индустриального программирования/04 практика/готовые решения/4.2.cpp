// Один пожилой учитель математики написал на доске последовательность, в которой первый элемент равен 1, а последующие построены по определённому правилу:
// 1
// 11
// 21
// 1211
// 111221
// 312211
// 13112221
// ...
// Необходимо написать программу, которая на вход получает число Х (первый член последовательности) и число N (количество элементов), 
// а выводит N-ую строку соответствующем заданному числу последовательности, 
// пока учитель ищет глицин и пытается вспомнить правило построения данной последовательности.

// Формат ввода
// 1 4

// Формат вывода
// 1211

#include <iostream>
#include <string>
using namespace std;

// Функция для генерации следующего элемента последовательности
string generateNextSequenceElement(const string& current) {
    string result = "";
    int count = 1;
    
    for (int i = 0; i < current.length(); i++) {
        // Считаем количество одинаковых символов в текущей последовательности
        while (i + 1 < current.length() && current[i] == current[i + 1]) {
            count++;
            i++;
        }
        
        // Добавляем количество символов и сам символ в новую последовательность
        result += to_string(count) + current[i];
        count = 1; // Сбрасываем счетчик
    }
    
    return result;
}

int main() {
    int x, n;
    cin >> x >> n;
    
    string sequence = to_string(x); // Преобразуем первый элемент в строку
    
    // Генерируем последовательность до нужной длины
    for (int i = 1; i < n; i++) {
        sequence = generateNextSequenceElement(sequence);
    }
    
    cout << sequence << endl;
    
    return 0;
}
